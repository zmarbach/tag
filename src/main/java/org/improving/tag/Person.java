package org.improving.tag;

import java.util.Date;

public class Person {
    private String name;
    private Date dob;
    private String address;
    private String city;
    private String state;
    private String zip;

    public String getName() {
        return name;
    }
    public Date getDob() {
        return dob;
    }
    public String getAddress() {
        return address;
    }
    public String getCity() {
        return city;
    }
    public String getState() {
        return state;
    }
    public String getZip() {
        return zip;
    }
}


